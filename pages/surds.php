<h2>The Laws of Indices</h2>
<p>
When manipulating indices, there are a number of laws that will help you make use of them in your algebra:
</p>
<ul>
<li><h3>Rule 1:</h3> 
<h4>Multiplying values with the same base but different exponents is the same as adding exponents</h4>

\[ {\color{#D20}{a}}{\color{#02D}{^b}}×{\color{#D20}{a}}{\color{#02D}{^c}} = {\color{#D20}{a}}{\color{#02D}{^{b+c}}} \]

Here's an example:

\[ {\color{#D20}{2}}{\color{#02D}{^3}} × {\color{#D20}{2}}{\color{#02D}{^5}} =  ({\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}})×({\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}) \]
\[ = {\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}}×{\color{#D20}{2}} \]
\[ = {\color{#D20}{2}}^{\color{#02D}{8}} \]

</p>
</li>
<li><h3>Rule 2:</h3> 
<h4>Dividing values with the same base but different exponents is the same as subtracting exponents</h4>


\[ {\color{#D20}{a}}{\color{#02D}{^b}} ÷ {\color{#D20}{a}}{\color{#02D}{^c}} = {\color{#D20}{a}}{\color{#02D}{^{b-c}}} \]

Here's an example:

\[ {\color{#D20}{2}}{\color{#02D}{^5}} ÷ {\color{#D20}{2}}{\color{#02D}{^3}} =  {\color{#D20}{2}}{\color{#02D}^{5-3}} \]
\[{\color{#D20}{32}} ÷ {\color{#D20}{8}} = {\color{#D20}{2}}{\color{#02D}{^2}}\]
\[= {\color{#D20}{4}}\]

</li>

</li>
<li><h3>Rule 3:</h3> 
<h4>A value raised by an exponent and then raised by an exponent is the same as multiplying both exponents</h4>
\[ ({\color{#D20}{a}}{\color{#02D}{^b}})^c = {\color{#D20}{a}}{\color{#02D}{^{b*c}}} \]

</li>


</li>
<li><h3>Rule 4:</h3> 
<h4>A value raised by zero equals '1'</h4>

\[ {\color{#D20}{a}}{\color{#02D}{^b}}×{\color{#D20}{a}}{\color{#02D}{^0}} = {\color{#D20}{a}}{\color{#02D}{^{b+0}}} = {\color{#D20}{a}}{\color{#02D}{^{b}}} \]
\[ \therefore \]
\[ {\color{#D20}{a}}{\color{#02D}{^0}}=1\]
</li>

<li><h3>Rule 5:</h3> 
<h4>A raised value multiplied by a value raised by the negative of the same exponent equals 1, therefore the negative exponent is the same as the reciprocal of the positive exponent</h4>

\[ {\color{#D20}{a}}{\color{#02D}{^b}}×{\color{#D20}{a}}{\color{#02D}^{-b}} = {\color{#D20}{a}} ^ {\color{#02D}{b - b}}  = {\color{#D20}{a}} ^ {\color{#02D}{0}} = 1  \]

\[ \therefore \]
\[ {\color{#D20}{a}}^{\color{#02D}{-b}} = {1 \over {{\color{#D20}{a}}^{\color{#02D}{b}}}}\]

</li>
</ul>
